#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;

	if (ac == 0)
	{
		return (0);
	}
	while (ac > 1)
	{
		i = -1;
		while (av[ac - 1][++i])
		{
			write(1, &av[ac - 1][i], 1);
		}
		write(1, "\n", 1);
		ac--;
	}
	return (0);
}
